    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Abaixo há uma caixa de confirmação, caso o usuario clique em sim, será removido a linha selecionada na tabela.
        int confirma = JOptionPane.showConfirmDialog(this,"Você tem certeza que deseja remover a disciplina "+jComboBoxDisciplinas.getSelectedItem()+" ?");
        if(confirma == 0){ // 0 é o retorno caso o usuario clique sim.
            try{
                String cmd = "DELETE FROM disciplina where nomeDisciplina = '"+jComboBoxDisciplinas.getSelectedItem()+"'"; // Removendo os valores com a instrução delete
                Connection con = ConexaoMySQL.getInstance().getConnection(); // se conectando ao banco de dados
                con.createStatement().executeUpdate(cmd); // executando a instrução delete
                JOptionPane.showMessageDialog(this, "A disciplina "+jComboBoxDisciplinas.getSelectedItem()+" foi deletada do banco de dados."); // uma mensagem de informação, dizendo que a disciplina que foi selecionada foi removida
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

