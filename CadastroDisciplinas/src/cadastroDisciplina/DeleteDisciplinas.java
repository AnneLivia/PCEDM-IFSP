    private void loadCombo()
    {
        try{
            String cmd = "SELECT * FROM Disciplina"; // Instrução necessaria para selecionar todos os valores do banco de dados
            Connection con = ConexaoMySQL.getInstance().getConnection(); // fazendo a conexão como banco de dados
            ResultSet res = con.createStatement().executeQuery(cmd); // recebendo o resultado da instrução
            while(res.next()) // enquanto houver valores para ser capturados da tabela, este loop será executado
            {
                String nomeDisDB = res.getString("nomeDisciplina"); // capturando os valores da coluna nomeDisciplina
                ((DefaultComboBoxModel)jComboBoxDisciplinas.getModel()).addElement(nomeDisDB); // inserindo este valor no combobox
            }
        }catch(SQLException ex)
        {
            ex.printStackTrace();
        }
        
    }
